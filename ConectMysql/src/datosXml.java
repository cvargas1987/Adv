
public class datosXml {
	
	private String ip;
	private String bd; 
	private String us;
	private String pass; 
	
	public datosXml () {
		
	}
	
	public String getIp() {
		return ip;
		
	}
	
	public String getBd() {
		return bd; 
	}

	public String getUs() {
		return us; 
	}
	public String getPass() {
		return pass; 
	}

	
	
	public void setIp(String ip) {
		this.ip = ip;
		
	}
	
	public void setBd(String bd) {
		this.bd = bd; 
	}
	
	public void setUs(String us) {
		this.us = us; 
	}

	public void setPass(String pass) {
		this.pass = pass; 
	}

	
}
